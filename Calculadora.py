#TIPO DE OPERACAO (+,-,*,/)

n1 = float(input("digite o primeiro número:"))
n2 = float(input("digite o primeiro segundo:"))
operacao =input("informe a operação:")
 

if  operacao== "+":
   print(n1+n2)

elif operacao== "-": 
    print(n1-n2)
elif operacao== "*": 
     print(n1*n2 )
elif operacao == '/':
     print(n1/n2)
else:
    print('opcao invalida')



      

